package com.washics18.Condominio.model;

public class RoleCondo {

	private Condo condo;
	private String role;
	
	
	public Condo getCondo() {
		return this.condo;
	}
	
	public void setCondo(Condo pCondo) {
		this.condo = pCondo;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String pRole) {
		this.role = pRole;
	}
	
}
